public class StringEquality {
	
	// main method
	public static void main(String[] args) {
		
		// taking user input
		String s1 = "Kapil";
		String s2 = "Kapil";
		String s3 = "Abhi";
		
		// checking for equality
		boolean val1 = s2.equals(s1);
		boolean val2 = s2.equals(s3);
		
		// prints the return value
		System.out.println("String2 is equal to string1 = " + val1);
		System.out.println("String2 is equal to string3 = " + val2);


	 }
		

}
